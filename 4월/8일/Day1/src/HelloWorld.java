
public class HelloWorld {

	public static void main(String[] args) {
		// main 함수 안에서 코드의 시작이 된다.
		
		// 컴퓨터가 알아듣는 숫자 0 1 이진수
		// 컴퓨터의 최소 단위 = bit
		
		// 컴파일 : 자바로 코드작성하면 이진수로 변경
		
		// 값(데이터)이 들어오면 어떤 타입, 어떤 모양 구별하여 메모리(저장공간)에 공간 만들때 필요한 자료형
		// 정수 : 소수점이 없는 숫자(int) 4byte
		// 실수 : 소수점이 있는 숫자 (double) 8byte
		// 문자열 : String
		// 문자 : char 2byte =  아스키코드가 아니라 유니코드 값을 이용
		
		System.out.println(10);
		System.out.println(1.1);
		System.out.println("안녕하세요");
		System.out.println('B');
		
	}

}
